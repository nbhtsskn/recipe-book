import json

with open('c:/Users/sasak/project/recipes/recipes.json', 'r', encoding='utf-8') as f:
    index = json.load(f)

recipes_js = json.dumps(index, ensure_ascii=False, indent=2)

html = (
    '<!DOCTYPE html>\n'
    '<html lang="ja">\n'
    '<head>\n'
    '  <meta charset="UTF-8">\n'
    '  <meta name="viewport" content="width=device-width, initial-scale=1.0">\n'
    '  <title>My Recipe Book</title>\n'
    '  <link rel="stylesheet" href="assets/style.css">\n'
    '</head>\n'
    '<body>\n'
    '  <header class="site-header">\n'
    '    <h1>\U0001f373 My Recipe Book</h1>\n'
    '    <p>\u52d5\u753b\u304b\u3089\u96c6\u3081\u305f\u30ec\u30b7\u30d4\u30b3\u30ec\u30af\u30b7\u30e7\u30f3</p>\n'
    '    <div class="search-bar">\n'
    '      <input type="text" id="search" placeholder="\u30ec\u30b7\u30d4\u3092\u691c\u7d22... / Search recipes...">\n'
    '    </div>\n'
    '  </header>\n'
    '  <main class="main-content">\n'
    '    <p class="section-title">\u30ec\u30b7\u30d4\u4e00\u89a7 <span id="recipe-count"></span></p>\n'
    '    <div class="recipe-grid" id="recipe-grid"></div>\n'
    '    <div class="empty-state" id="empty-state" style="display:none">\n'
    '      <div class="icon">\U0001f4ed</div>\n'
    '      <p>\u30ec\u30b7\u30d4\u304c\u898b\u3064\u304b\u308a\u307e\u305b\u3093</p>\n'
    '    </div>\n'
    '  </main>\n'
    '  <script>\n'
    '    const allRecipes = ' + recipes_js + ';\n'
    '\n'
    '    function escapeHtml(str) {\n'
    '      return String(str || "").replace(/&/g,"&amp;").replace(/</g,"&lt;").replace(/>/g,"&gt;").replace(/"/g,"&quot;");\n'
    '    }\n'
    '\n'
    '    function renderGrid(recipes) {\n'
    '      const grid = document.getElementById("recipe-grid");\n'
    '      const empty = document.getElementById("empty-state");\n'
    '      document.getElementById("recipe-count").textContent = "(" + allRecipes.length + ")";\n'
    '      if (recipes.length === 0) {\n'
    '        grid.innerHTML = "";\n'
    '        empty.style.display = "block";\n'
    '        return;\n'
    '      }\n'
    '      empty.style.display = "none";\n'
    '      grid.innerHTML = recipes.map(function(r) {\n'
    '        var thumb = r.thumbnail ? \'<img src="\' + r.thumbnail + \'" alt="\' + escapeHtml(r.title) + \'" loading="lazy" onerror="this.style.display=\\\'none\\\'">\' : "";\n'
    '        var dur = r.duration ? "<span>\u23f1 " + r.duration + "</span>" : "";\n'
    '        var tags = (r.tags || []).slice(0, 4).map(function(t) { return \'<span class="tag">\' + escapeHtml(t) + "</span>"; }).join("");\n'
    '        return \'<a class="recipe-card" href="recipes/\' + r.id + \'.html">\'\n'
    '          + thumb\n'
    '          + \'<div class="card-body">\'\n'
    '          + \'<div class="card-title">\' + escapeHtml(r.title) + "</div>"\n'
    '          + \'<div class="card-meta"><span>\U0001f4fa \' + escapeHtml(r.channel || "") + "</span>" + dur + \'<span>\U0001f4c5 \' + (r.added_at || "") + "</span></div>"\n'
    '          + \'<div class="card-tags">\' + tags + "</div>"\n'
    '          + "</div></a>";\n'
    '      }).join("");\n'
    '    }\n'
    '\n'
    '    document.getElementById("search").addEventListener("input", function(e) {\n'
    '      var q = e.target.value.toLowerCase();\n'
    '      var filtered = allRecipes.filter(function(r) {\n'
    '        return r.title.toLowerCase().includes(q)\n'
    '          || (r.channel || "").toLowerCase().includes(q)\n'
    '          || (r.tags || []).some(function(t) { return t.toLowerCase().includes(q); });\n'
    '      });\n'
    '      renderGrid(filtered);\n'
    '    });\n'
    '\n'
    '    renderGrid(allRecipes);\n'
    '  </script>\n'
    '</body>\n'
    '</html>\n'
)

with open('c:/Users/sasak/project/index.html', 'w', encoding='utf-8') as f:
    f.write(html)
print('index.html updated. Recipes:', len(index))
